package _02variables;

public class TipoVar {
    public static void main(String[] args) {
        var nombre = "HOla";
        var precio = 334.5F;
        var isOpen = true;
        isOpen = false;
        System.out.println("nombre = " + nombre);
        System.out.println("precio = " + precio);
        System.out.println("isOpen = " + isOpen);
    }
}
